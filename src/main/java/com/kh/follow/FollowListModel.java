package com.kh.follow;

public class FollowListModel {

	private int follow;
	private int following;
	private String nickname;
	private String prof_img;
	private int follow_to;
	
	
	public int getFollow() {
		return follow;
	}
	public void setFollow(int follow) {
		this.follow = follow;
	}
	public int getFollowing() {
		return following;
	}
	public void setFollowing(int following) {
		this.following = following;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getProf_img() {
		return prof_img;
	}
	public void setProf_img(String prof_img) {
		this.prof_img = prof_img;
	}
	public int getFollow_to() {
		return follow_to;
	}
	public void setFollow_to(int follow_to) {
		this.follow_to = follow_to;
	}
	
}
